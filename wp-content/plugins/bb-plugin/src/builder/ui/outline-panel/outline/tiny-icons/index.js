export const getModuleIconComponent = ( type, settings, hasDefinition = true ) => {

	const moduleDefs = FLBuilderConfig.contentItems.module

	if ( ! hasDefinition ) {
		return Alert
	}

	switch ( type ) {
	case 'rich-text':
		return Text
	case 'photo':
		return Photo
	case 'icon':
		if ( 'icon' in settings ) {
			return () => <i className={ settings.icon } aria-hidden="true"></i>
		}
		break
	default:

		const def = moduleDefs.find( def => type === def.slug )
		if ( def && 'icon' in def ) {
			return () => (
				<div
					style={ {
						flexShrink: 0,
						width: 16,
						height: 16,
						fill: 'currentColor'
					} }
					dangerouslySetInnerHTML={ { __html: def.icon } }
				/>
			)
		}
		return DefaultIcon
	}
}

